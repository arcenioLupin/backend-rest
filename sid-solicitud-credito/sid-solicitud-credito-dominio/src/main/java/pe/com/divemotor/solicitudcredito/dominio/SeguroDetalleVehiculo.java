public class SeguroDetalleVehiculo {

    private String numPedidoVeh;
    private String descripcion;
    private String desTipoActividad;
    private String numPlacaVeh;
    private String numMotorVeh;
    private String numChasis;
    private String canNroAsie;
    private String codGarantia;
    /**
     * @return the descripcion
     */
    public String getDescripcion() {
        return descripcion;
    }

    /**
     * @param descripcion the descripcion to set
     */
    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }

    /**
     * @return the desTipoActividad
     */
    public String getDesTipoActividad() {
        return desTipoActividad;
    }

    /**
     * @param desTipoActividad the desTipoActividad to set
     */
    public void setDesTipoActividad(String desTipoActividad) {
        this.desTipoActividad = desTipoActividad;
    }

    /**
     * @return the numPlacaVeh
     */
    public String getNumPlacaVeh() {
        return numPlacaVeh;
    }

    /**
     * @param numPlacaVeh the numPlacaVeh to set
     */
    public void setNumPlacaVeh(String numPlacaVeh) {
        this.numPlacaVeh = numPlacaVeh;
    }

    /**
     * @return the numMotorVeh
     */
    public String getNumMotorVeh() {
        return numMotorVeh;
    }

    /**
     * @param numMotorVeh the numMotorVeh to set
     */
    public void setNumMotorVeh(String numMotorVeh) {
        this.numMotorVeh = numMotorVeh;
    }

    /**
     * @return the numChasis
     */
    public String getNumChasis() {
        return numChasis;
    }

    /**
     * @param numChasis the numChasis to set
     */
    public void setNumChasis(String numChasis) {
        this.numChasis = numChasis;
    }

    /**
     * @return the canNroAsie
     */
    public String getCanNroAsie() {
        return canNroAsie;
    }

    /**
     * @param canNroAsie the canNroAsie to set
     */
    public void setCanNroAsie(String canNroAsie) {
        this.canNroAsie = canNroAsie;
    }

    /**
     * @return the numPedidoVeh
     */
    public String getNumPedidoVeh() {
        return numPedidoVeh;
    }

    /**
     * @param numPedidoVeh the numPedidoVeh to set
     */
    public void setNumPedidoVeh(String numPedidoVeh) {
        this.numPedidoVeh = numPedidoVeh;
    }

    public String getCodGarantia() {
        return codGarantia;
    }

    public void setCodGarantia(String codGarantia) {
        this.codGarantia = codGarantia;
    }
     
    
}
