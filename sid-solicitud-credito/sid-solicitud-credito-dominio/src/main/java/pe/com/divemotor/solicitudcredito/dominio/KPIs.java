package pe.com.divemotor.solicitudcredito.dominio;

/**
 *
 * @author jaltamirano
 */
public class KPIs {

    private String codDetalle;
    private String codKpi;
    private String desKpi;
    private String valKpi;
    private String anio;
    private String mes;
    private String codZona;
    private String desZona;
    private String codAreaVenta;
    private String desAreaVenta;

    public String getCodDetalle() {
        return codDetalle;
    }

    public void setCodDetalle(String codDetalle) {
        this.codDetalle = codDetalle;
    }

    public String getCodKpi() {
        return codKpi;
    }

    public void setCodKpi(String codKpi) {
        this.codKpi = codKpi;
    }

    public String getDesKpi() {
        return desKpi;
    }

    public void setDesKpi(String desKpi) {
        this.desKpi = desKpi;
    }

    public String getValKpi() {
        return valKpi;
    }

    public void setValKpi(String valKpi) {
        this.valKpi = valKpi;
    }

    public String getAnio() {
        return anio;
    }

    public void setAnio(String anio) {
        this.anio = anio;
    }

    public String getMes() {
        return mes;
    }

    public void setMes(String mes) {
        this.mes = mes;
    }

    public String getCodZona() {
        return codZona;
    }

    public void setCodZona(String codZona) {
        this.codZona = codZona;
    }

    public String getDesZona() {
        return desZona;
    }

    public void setDesZona(String desZona) {
        this.desZona = desZona;
    }

    public String getCodAreaVenta() {
        return codAreaVenta;
    }

    public void setCodAreaVenta(String codAreaVenta) {
        this.codAreaVenta = codAreaVenta;
    }

    public String getDesAreaVenta() {
        return desAreaVenta;
    }

    public void setDesAreaVenta(String desAreaVenta) {
        this.desAreaVenta = desAreaVenta;
    }

}
